#Creating Strings

#Concetenating two strings
greeting = "Hello"
name = "Zoey"
message = greeting + "," + name + "!"
print(message)

#Repeating a string
laugh = "Ha"
repeat_laugh = laugh * 3
print(repeat_laugh)

#Accessing a character in a string
word = "Python"
letter = word[0]
print(letter)

#Slicing a string
word = "Hello, World!"
sub_word = word[7 : 13]
print(sub_word)
